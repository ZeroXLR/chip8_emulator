pub struct Memory{
	values: [u8; MAX_BYTES]
}

impl Memory {
	pub fn new() -> Memory {
		Memory{
			values: INITIAL_VALUES
		}
	}

	pub fn write_data_at(&mut self, address: usize, code: &[u8]) {
		self.values[address .. address + code.len()].copy_from_slice(code);
	}
}

impl ops::Index<usize> for Memory {
	type Output = u8;

	fn index(&self, address: usize) -> &u8 {
		&self.values[address]
	}
}

impl ops::Index<ops::Range<usize>> for Memory {
	type Output = [u8];

	fn index(&self, range: ops::Range<usize>) -> &[u8] {
		&self.values[range]
	}
}

impl ops::IndexMut<usize> for Memory {
	fn index_mut(&mut self, address: usize) -> &mut u8 {
		&mut self.values[address]
	}
}

impl fmt::Debug for Memory {
	fn fmt(&self, f: &mut fmt::Formatter) -> fmt::Result {
		write!(
			f,
			"Memory: {}\n",
			self.values.iter().map( |b| format!("{:#X} ", b) ).collect::<String>()
		)
	}
}